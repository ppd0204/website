<h2>Data</h2>

OSM Buildings uses buildings data from <a href="http://openstreetmap.org">OpenStreetMap</a> the free wiki world map which is available under the <a href="http://opendatacommons.org/licenses/odbl/">OpenDatabase License (ODbL)</a>.

OSM Buildings also incorporates govermental open data in a complementray way. Such data is converted and served through the OSM Buildings API, but is not part of OpenStreetMaps.

<h2>Maps</h2>

On our site we also display OpenStreetMap base maps (below the 3D buildings). These are styled and hosted by <a href="http://www.mapbox.com">MapBox</a> and <a href="http://www.mapquest.com">MapQuest</a>
